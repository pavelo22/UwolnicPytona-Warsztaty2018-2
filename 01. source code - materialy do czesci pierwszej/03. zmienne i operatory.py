
print('Oryginalny tekst:', 'ala mA kota') 

napis = "ala mA kota".capitalize()
print('Przykład na capitalize():', napis)

napis = "ala mA kota".upper()
print('Przykład na upper():', napis)

napis = "ala mA kota".lower()
print('Przykład na lower():', napis)

napis = "ala mA kota".title()
print('Przykład na title():', napis)

napis = "ala mA kota"
print('Przykład na isalpha() ostatniego znaku:', napis[-1].isalpha())
print('Przykład na isdigit() ostatniego znaku:', napis[-1].isdigit())
